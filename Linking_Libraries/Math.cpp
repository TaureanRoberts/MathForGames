#include "Math.h"

float Math1::Add(float lhs, float rhs)
{
	float other;
	other = lhs + rhs;
	return other;
}

float Math1::Subtract(float lhs, float rhs)
{
	float other;
	other = lhs + rhs;
	return other;
}

float Math1::Multiply(float lhs, float rhs)
{
	float other;
	other = lhs * rhs;
	return other;
}

float Math1::Divide(float lhs, float rhs)
{
	float other;
	other = lhs / rhs;
	return other;
}
