#include "MyMath.h"
#include <iostream>

float Math::Add(float lhs, float rhs)
{
	float other;
	other = lhs + rhs;
	return other;
}

float Math::Subtract(float lhs, float rhs)
{
	float other;
	other = lhs + rhs;
	return other;
}

float Math::Multiply(float lhs, float rhs)
{
	float other;
	other = lhs * rhs;
	return other;
}

float Math::Divide(float lhs, float rhs)
{
	float other;
	other = lhs / rhs;
	return other;
}
